SUBROUTINE JACOBIAN(XX,H,JJ) 
!------------------------------------------------------------------------------------------    
IMPLICIT NONE
!------------------------------------------------------------------------------------------    
! F_1 = 3x+2y+5
! F_2 = -3xy+3x^2 +y
DOUBLE PRECISION    :: FFP1(2),FFN1(2),FFP2(2),FFN2(2),XX(2),H,JJ(2,2)
!------------------------------------------------------------------------------------------    


CALL FUNC(XX(1)+H,XX(2),FFP1)
CALL FUNC(XX(1)-H,XX(2),FFN1)
CALL FUNC(XX(1),XX(2)+H,FFP2)
CALL FUNC(XX(1),XX(2)-H,FFN2)


JJ(1,1) = (FFP1(1)-FFN1(1))/(2*H)
JJ(1,2) = (FFP2(1)-FFN2(1))/(2*H)
JJ(2,1) = (FFP1(2)-FFN1(2))/(2*H)
JJ(2,2) = (FFP2(2)-FFN2(2))/(2*H)

RETURN
END
